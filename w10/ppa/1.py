class Calculator:
    def Sum(a,b):
        return a+b
    def Multiply(a,b):
        return a*b
    def Subtraction(a,b):
        return a-b
    def Division(a,b):
        return a/b
    def Remainder(a,b):
        return a%b
    def Power(a,b):
        return a**b
    def Quotent(a,b):
        return a//b

         